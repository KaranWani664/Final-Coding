// Break - Stop Loop from that given point

for (let i = 1; i<20; i++){
    if (i === 11) break;
    else console.log(i);   
}

console.log("");

// Continue - Skip Current/that given Iteration & goes to next in Loop i.e. i=4

for (let i = 50; i<60; i++){
    if (i === 54) continue;
    else console.log(i);   
}